package negocio;


public class Habitacion {
    private int k_numeroHabitacion;
    private String k_tipo;
    private int k_hotel;

    public int getK_numeroHabitacion() {
        return k_numeroHabitacion;
    }

    public String getK_tipo() {
        return k_tipo;
    }

    public int getK_hotel() {
        return k_hotel;
    }

    public void setK_numeroHabitacion(int k_numeroHabitacion) {
        this.k_numeroHabitacion = k_numeroHabitacion;
    }

    public void setK_tipo(String k_tipo) {
        this.k_tipo = k_tipo;
    }

    public void setK_hotel(int k_hotel) {
        this.k_hotel = k_hotel;
    }
    
    
}
